class LRUCache {
private:
	struct CacheNode {
		int key;
		int value;
		CacheNode(int k, int v) :
				key(k), value(v) {
		}
	};

	list<CacheNode> cacheList;
	unordered_map<int, list<CacheNode>::iterator> cacheMap;
	int capacity;

public:
	LRUCache(int capacity) {
		this->capacity = capacity;
	}

	int get(int key) {
		if (cacheMap.find(key) == cacheMap.end())
			return -1;
		cacheList.splice(cacheList.begin(), cacheList, cacheMap[key]);
		cacheMap[key] = cacheList.begin();
		return cacheMap[key]->value;
	}

	void set(int key, int value) {
		//unordered_map<int, list<CacheNode>::iterator>::const_iterator it;
		auto it = cacheMap.find(key);

		if (it == cacheMap.end()) {
			if (cacheList.size() == capacity) {
				cacheMap.erase(cacheList.back().key);
				cacheList.pop_back();
			}
			cacheList.push_front(CacheNode(key, value));
			cacheMap[key] = cacheList.begin();
		} else {
			cacheList.erase(cacheMap[key]);
			//	cacheList.erase(it->second);
			cacheMap.erase(key);

			cacheList.push_front(CacheNode(key, value));
			cacheMap[key] = cacheList.begin();

		}
	}

public:

	vector<vector<int> > permuteUnique(vector<int> &num) {

		vector<vector<int>> result;
		vector<int> path;
		vector<int> visited(num.size() + 1, 0);

		if (num.size() == 0) return result;

		sort(num.begin(), num.end());
		generate_permutation(num, 0, visited, path, result);

		return result;
	}


	void generate_permutation(vector<int> &num, int step,
			                  vector<int> &visited,
							  vector<int> &path, vector<vector<int>> &result)
	{
		if (step == num.size()) {
			result.push_back(path);
			return;
		}

		for (int i = 0; i < num.size(); i++) {
			if (visited[i] == 0) {
				if (i > 0 && num[i] == num[i - 1] && visited[i - 1] == 0) {
					continue;
				}
				visited[i] = 1;
				path.push_back(num[i]);
				generate_permutation(num, step + 1, visited,
						             path, result);
				path.pop_back();
				visited[i] = 0;
			}
		}

	}
};




